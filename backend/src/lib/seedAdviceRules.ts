export async function seedAdviceRules() {
  for (const rule of RULES) {
    await prisma.financialAdviceRule.upsert({
      where: { ratioKey_state: { ratioKey: rule.ratioKey, state: rule.state } },
      update: { advice: rule.advice, category: rule.category },
      create: rule,
    })
  }
  console.log('✅ Financial advice rules seeded:', RULES.length, 'rules')
}
